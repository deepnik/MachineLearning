# For Loop

# Ist Program
print("Result of 1st Program is: ")
for i in range(0,10):
    print(i,end=" ")
    print("Hello",end="\n")
    
# 2nd Program
a=[5,2,4,8]
print("\nResult of 2nd Program is: ")
for i in a:
    print(i,end="\n")


